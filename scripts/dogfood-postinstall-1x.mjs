#!/usr/bin/env node
/**
 * Dogfood script for the 1.x → 2.0 postinstall auto-upgrade.
 *
 * Slice: 2026-06-12-postinstall-1x-detector-tdd.
 *
 * This script:
 *   1. Creates a temp project + temp home directory
 *      containing a 1.x peaks-cli fixture (legacy global
 *      config, dev-preference.md referencing 'peaks progress',
 *      missing .peaks/preferences.json).
 *   2. Imports `detect1xProjectState` and
 *      `autoUpgrade1xProjectIfPresent` from
 *      `scripts/install-skills.mjs`.
 *   3. Invokes both functions against the fixture (with
 *      the spawn side-effect intercepted — the dogfood does
 *      not invoke the real peaks binary; it asserts the
 *      detection fires and the auto-upgrade is dispatched,
 *      but the actual spawn is replaced with a no-op).
 *   4. Writes a markdown report to
 *      `.peaks/_runtime/<sessionId>/rd/postinstall-1x-dogfood.md`.
 *
 * Why mock the spawn:
 *   - The dogfood is about the DETECTION + DISPATCH contract,
 *     not about whether the umbrella's 7 sub-steps all pass
 *     (that is the umbrella's own e2e coverage).
 *   - Spawning the real `peaks` binary in a temp dir is
 *     brittle (depends on PATH, on the umbrella's path
 *     resolution, on the install hooks not running in the
 *     test fixture). The dispatch-shape contract is what
 *     matters for the "auto-upgrade fires on install" claim.
 */
import { spawnSync } from 'node:child_process';
import { existsSync, mkdirSync, mkdtempSync, rmSync, writeFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join, resolve } from 'node:path';
import { pathToFileURL } from 'node:url';

const SCRIPT_URL = pathToFileURL(resolve('scripts/install-skills.mjs')).href;
const scriptModule = await import(SCRIPT_URL);
const { detect1xProjectState, autoUpgrade1xProjectIfPresent } = scriptModule;

const sessionId = process.env.PEAKS_SESSION_ID ?? '2026-06-11-session-35ee92';
const reportDir = resolve(`.peaks/_runtime/${sessionId}/rd`);
const reportPath = join(reportDir, 'postinstall-1x-dogfood.md');

function buildFixture() {
  const projectRoot = mkdtempSync(join(tmpdir(), 'peaks-1x-dogfood-project-'));
  const home = mkdtempSync(join(tmpdir(), 'peaks-1x-dogfood-home-'));

  // Signal 1: 1.x global config
  mkdirSync(join(home, '.peaks'), { recursive: true });
  writeFileSync(
    join(home, '.peaks', 'config.json'),
    JSON.stringify({ version: '1.4.2', language: 'en' }, null, 2),
    'utf8'
  );

  // Signal 2: .peaks/_runtime/ to anchor projectRoot
  mkdirSync(join(projectRoot, '.peaks', '_runtime'), { recursive: true });

  // Signal 3: dev-preference.md with 'peaks progress'
  mkdirSync(join(projectRoot, '.claude', 'rules', 'common'), { recursive: true });
  writeFileSync(
    join(projectRoot, '.claude', 'rules', 'common', 'dev-preference.md'),
    '# dev-preference\n\nWe use **peaks progress** as the per-step completion metric.\n',
    'utf8'
  );

  // Signal 4: missing preferences.json (1.x never migrated)

  return { projectRoot, home };
}

function buildTwoXFixture() {
  const projectRoot = mkdtempSync(join(tmpdir(), 'peaks-2x-dogfood-project-'));
  const home = mkdtempSync(join(tmpdir(), 'peaks-2x-dogfood-home-'));

  mkdirSync(join(projectRoot, '.peaks', '_runtime'), { recursive: true });
  mkdirSync(join(projectRoot, '.peaks'), { recursive: true });
  mkdirSync(join(home, '.peaks'), { recursive: true });
  writeFileSync(
    join(projectRoot, '.peaks', 'preferences.json'),
    JSON.stringify({ schema_version: '2.0.0' }, null, 2),
    'utf8'
  );
  writeFileSync(
    join(home, '.peaks', 'config.json'),
    JSON.stringify({ version: '2.0.0' }, null, 2),
    'utf8'
  );

  return { projectRoot, home };
}

async function main() {
  const report = [];
  report.push(`# Postinstall 1.x auto-upgrade — dogfood report (${new Date().toISOString()})`);
  report.push('');
  report.push('> Slice: 2026-06-12-postinstall-1x-detector-tdd');
  report.push(
    '> This report was generated by `node scripts/dogfood-postinstall-1x.mjs`.'
  );
  report.push(
    '> It proves the `detect1xProjectState` and `autoUpgrade1xProjectIfPresent`'
  );
  report.push(
    '> functions in `scripts/install-skills.mjs` fire correctly on a 1.x fixture.'
  );
  report.push('');

  // --- Scenario A: 1.x project state — detection + dispatch ---
  const oneX = buildFixture();
  report.push('## Scenario A — 1.x project state (detection + dispatch)');
  report.push('');
  report.push('**Fixture**');
  report.push(`- projectRoot: \`${oneX.projectRoot}\``);
  report.push(`- HOME: \`${oneX.home}\``);
  report.push(
    `- 1.x signals planted: \`~/.peaks/config.json\` (1.4.2), \`dev-preference.md\` (peaks progress), missing \`.peaks/preferences.json\``
  );
  report.push('');

  // detect1xProjectState
  const stateA = detect1xProjectState(oneX.projectRoot);
  report.push('### `detect1xProjectState` result');
  report.push('');
  report.push('```json');
  report.push(JSON.stringify(stateA, null, 2));
  report.push('```');
  report.push('');
  report.push(
    `> isOneX: **${stateA.isOneX}** | signals.length: **${stateA.signals.length}** | projectRoot: **${stateA.projectRoot}**`
  );
  report.push('');

  // autoUpgrade1xProjectIfPresent — but with spawnSync mocked
  // to avoid invoking the real peaks binary. We intercept
  // the import's spawnSync by monkey-patching
  // node:child_process.spawnSync for the duration of the
  // call. This is a runtime monkey-patch (not vitest
  // mocking) because this script runs with plain `node`.
  const originalSpawnSync = spawnSync;
  const intercepted = [];
  const fakeSpawnSync = (...args) => {
    intercepted.push(args);
    return {
      pid: 9999,
      output: ['', 'fake upgrade ok\n', ''],
      stdout: 'fake upgrade ok\n',
      stderr: '',
      status: 0,
      signal: null,
    };
  };
  // The install-skills.mjs autoUpgrade1xProjectIfPresent
  // does `import { spawnSync } from 'node:child_process'`
  // at the top of the module. That import is captured once.
  // To intercept we monkey-patch the underlying module's
  // export. node:child_process exports a binding object
  // whose spawnSync can be reassigned in CJS-style, but
  // ESM exports are usually read-only. Workaround: replace
  // the spawnSync property on the cached module record.
  // Simpler: we use the `child_process` module-level
  // monkey-patch via the `node:module` cache. For a
  // .mjs script that does `import { spawnSync } from
  // 'node:child_process'`, the binding is live-bound
  // to the module's named export, and reassigning that
  // export's underlying function does affect the caller.
  try {
    const cpModule = await import('node:child_process');
    cpModule.spawnSync = fakeSpawnSync;
  } catch (err) {
    report.push(
      `> **NOTE**: could not monkey-patch node:child_process.spawnSync (${err.message}). Falling back to real spawn — the actual dispatch contract is still observable in the report.`
    );
  }

  const resultA = await autoUpgrade1xProjectIfPresent({ cwd: oneX.projectRoot });

  // Restore the original spawnSync
  try {
    const cpModule = await import('node:child_process');
    cpModule.spawnSync = originalSpawnSync;
  } catch {
    // ignore
  }

  report.push('### `autoUpgrade1xProjectIfPresent` result');
  report.push('');
  report.push('```json');
  report.push(JSON.stringify(resultA, null, 2));
  report.push('```');
  report.push('');
  report.push(`> ran: **${resultA.ran}** | reason: **${resultA.reason}**`);
  if (intercepted.length > 0) {
    report.push('');
    report.push('### Spawn calls intercepted');
    report.push('');
    report.push('```js');
    for (const args of intercepted) {
      const [cmd, cmdArgs] = args;
      report.push(`${cmd} ${JSON.stringify(cmdArgs)}`);
    }
    report.push('```');
  }
  report.push('');

  // Cleanup
  rmSync(oneX.projectRoot, { recursive: true, force: true });
  rmSync(oneX.home, { recursive: true, force: true });

  // --- Scenario B: 2.0 project state — auto-upgrade should NOT dispatch ---
  const twoX = buildTwoXFixture();
  report.push('## Scenario B — 2.0 project state (auto-upgrade should NOT dispatch)');
  report.push('');
  report.push('**Fixture**');
  report.push(`- projectRoot: \`${twoX.projectRoot}\``);
  report.push(`- HOME: \`${twoX.home}\``);
  report.push(
    `- 2.0 signals: \`~/.peaks/config.json\` (2.0.0), \`.peaks/preferences.json\` (schema_version 2.0.0)`
  );
  report.push('');

  const stateB = detect1xProjectState(twoX.projectRoot);
  report.push('### `detect1xProjectState` result');
  report.push('');
  report.push('```json');
  report.push(JSON.stringify(stateB, null, 2));
  report.push('```');
  report.push('');
  report.push(`> isOneX: **${stateB.isOneX}** (expected: false)`);
  report.push('');

  const resultB = await autoUpgrade1xProjectIfPresent({ cwd: twoX.projectRoot });
  report.push('### `autoUpgrade1xProjectIfPresent` result');
  report.push('');
  report.push('```json');
  report.push(JSON.stringify(resultB, null, 2));
  report.push('```');
  report.push('');
  report.push(`> ran: **${resultB.ran}** (expected: false)`);
  report.push('');

  // Cleanup
  rmSync(twoX.projectRoot, { recursive: true, force: true });
  rmSync(twoX.home, { recursive: true, force: true });

  // --- Verdict ---
  const verdict = {
    scenarioA_detect_isOneX: stateA.isOneX,
    scenarioA_signals_nonempty: stateA.signals.length > 0,
    scenarioA_projectRoot_resolved: stateA.projectRoot === oneX.projectRoot,
    scenarioA_autoUpgrade_ran: resultA.ran,
    scenarioB_detect_isOneX_false: stateB.isOneX === false,
    scenarioB_autoUpgrade_did_not_run: resultB.ran === false,
  };
  const allPass = Object.values(verdict).every((v) => v === true);
  report.push('## Verdict');
  report.push('');
  report.push('| Check | Result |');
  report.push('|---|---|');
  for (const [key, value] of Object.entries(verdict)) {
    report.push(`| ${key} | ${value ? '✅' : '❌'} |`);
  }
  report.push('');
  report.push(`**Overall: ${allPass ? '✅ PASS' : '❌ FAIL'}**`);
  report.push('');

  // Write the report
  mkdirSync(reportDir, { recursive: true });
  writeFileSync(reportPath, report.join('\n') + '\n', 'utf8');
  process.stdout.write(`Dogfood report written: ${reportPath}\n`);
  process.stdout.write(`Verdict: ${allPass ? 'PASS' : 'FAIL'}\n`);

  if (!allPass) {
    process.exitCode = 1;
  }
}

main().catch((err) => {
  process.stderr.write(`Dogfood script failed: ${err instanceof Error ? err.message : String(err)}\n`);
  process.exitCode = 1;
});
